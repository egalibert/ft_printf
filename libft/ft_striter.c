#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	i;
	int	y;

	i = 0;
	y = 0;
	if (s != NULL && f != NULL)
	{
		i = ft_strlen(s);
		while (y < i)
		{
			(*f)(s);
			s++;
			y++;
		}
	}
}
